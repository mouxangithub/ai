  function timezoneI18nKey(id) {
    return `timezoneOpt_${String(id).replace(/\//g, '_')}`;
  }

  // ja/ko: en base + locale overrides (no Chinese fallback)
  for (const loc of ['ja', 'ko']) {
    DICTIONARIES[loc] = { ...DICTIONARIES.en, ...DICTIONARIES[loc] };
  }

  function resolveLang() {
    const params = new URLSearchParams(location.search);
    const fromUrl = params.get('lang');
    if (fromUrl && DICTIONARIES[fromUrl]) return fromUrl;

    try {
      const fromStorage = localStorage.getItem('ui_lang');
      if (fromStorage && DICTIONARIES[fromStorage]) return fromStorage;
    } catch (e) {}

    const nav = (navigator.language || 'en').split('-')[0];
    if (DICTIONARIES[nav]) return nav;

    return 'en';
  }

  class I18n {
    constructor(lang) {
      this.lang = lang || resolveLang();
      this.dict = { ...DICTIONARIES.en, ...(DICTIONARIES[this.lang] || {}) };
      this.updateHtmlLang();
    }

    t(key, fallback) {
      return this.dict[key] ?? fallback ?? key;
    }

    tf(key, vars, fallback) {
      let s = this.t(key, fallback);
      if (vars && typeof vars === 'object') {
        for (const [k, v] of Object.entries(vars)) {
          s = s.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v ?? ''));
        }
      }
      return s;
    }

    getLang() {
      return this.lang;
    }

    setLang(lang) {
      if (!DICTIONARIES[lang]) return;
      this.lang = lang;
      this.dict = { ...DICTIONARIES.en, ...DICTIONARIES[lang] };
      try { localStorage.setItem('ui_lang', lang); } catch (e) {}
      this.updateHtmlLang();
    }

    updateHtmlLang() {
      document.documentElement.setAttribute('lang', this.lang);
    }

    static getSupportedLangs() {
      return Object.keys(DICTIONARIES);
    }

    static getDictionary(lang) {
      return DICTIONARIES[lang] || DICTIONARIES.en;
    }

    static getTimezoneIds() {
      return TIMEZONE_IDS.slice();
    }

    static timezoneKey(id) {
      return timezoneI18nKey(id);
    }
  }

  global.I18n = I18n;
